package com.viacao.vo;

public class OnibusVO {

	private Integer seqOnibus;
	private String tipo;
	private String empresa;
	private String placa;
	private Integer qtdPoltrona;
	
	public Integer getSeqOnibus() {
		return seqOnibus;
	}
	
	public void setSeqOnibus(Integer seqOnibus) {
		this.seqOnibus = seqOnibus;
	}
	
	public String getTipo() {
		return tipo;
	}
	
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	public String getEmpresa() {
		return empresa;
	}
	
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	
	public String getPlaca() {
		return placa;
	}
	
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	
	public Integer getQtdPoltrona() {
		return qtdPoltrona;
	}
	
	public void setQtdPoltrona(Integer qtdPoltrona) {
		this.qtdPoltrona = qtdPoltrona;
	}
}
