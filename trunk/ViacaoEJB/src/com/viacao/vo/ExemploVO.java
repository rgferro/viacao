package com.viacao.vo;

public class ExemploVO {
	
	private Integer seqExemplo;
	private String nomeExemplo;
	private String descricao;
	
	/**
	 * Obtém o atributo seqExemplo
	 * @return Valor do atributo seqExemplo
	 */
	public Integer getSeqExemplo() {
		return seqExemplo;
	}
	/**
	 * Altera o atributo seqExemplo
	 * @param seqExemplo Novo valor do atributo
	 */
	public void setSeqExemplo(Integer seqExemplo) {
		this.seqExemplo = seqExemplo;
	}
	/**
	 * Obtém o atributo nomeExemplo
	 * @return Valor do atributo nomeExemplo
	 */
	public String getNomeExemplo() {
		return nomeExemplo;
	}
	/**
	 * Altera o atributo nomeExemplo
	 * @param nomeExemplo Novo valor do atributo
	 */
	public void setNomeExemplo(String nomeExemplo) {
		this.nomeExemplo = nomeExemplo;
	}
	/**
	 * Obtém o atributo descricao
	 * @return Valor do atributo descricao
	 */
	public String getDescricao() {
		return descricao;
	}
	/**
	 * Altera o atributo descricao
	 * @param descricao Novo valor do atributo
	 */
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
