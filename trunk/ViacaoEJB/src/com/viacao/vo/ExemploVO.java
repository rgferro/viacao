package com.viacao.vo;

public class ExemploVO {
	
	Integer seqExemplo;
	String nomeExemplo;
	String descricao;
	
	public Integer getSeqExemplo() {
		return seqExemplo;
	}
	public void setSeqExemplo(Integer seqExemplo) {
		this.seqExemplo = seqExemplo;
	}
	public String getNomeExemplo() {
		return nomeExemplo;
	}
	public void setNomeExemplo(String nomeExemplo) {
		this.nomeExemplo = nomeExemplo;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
